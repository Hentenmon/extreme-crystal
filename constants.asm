	db \1
	dw \2
	ENDM

dn: MACRO
	db \1 << 4 + \2
	ENDM

; big-endian word
bigdw: MACRO
	dw ((\1)/$100) + (((\1)&$ff)*$100)
	ENDM

; address, bank
callab: MACRO
	ld hl, \1
	ld a, BANK(\1)
	rst FarCall
	ENDM

; bank, address
callba: MACRO
	ld a, BANK(\1)
	ld hl, \1
	rst FarCall
	ENDM

TX_RAM: MACRO
	db 1
	dw \1
	ENDM

TX_FAR: MACRO
	db $16
	dw \1
	db BANK(\1)
	ENDM

RGB: MACRO
	dw ((\3 << 10) | (\2 << 5) | (\1))
	ENDM

note: MACRO
	db \1
	ENDM

